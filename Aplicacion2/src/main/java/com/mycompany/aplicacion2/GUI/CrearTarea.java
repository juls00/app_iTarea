    private void titulo1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_titulo1MousePressed
         if(titulo1.getText().equals("Nombre")){
            titulo1.setText("");
            titulo1.setForeground(Color.black);
        }
         
         if(descripcion1.getText().isEmpty()){
             descripcion1.setText("Descripcion");
            descripcion1.setForeground(Color.gray);
         }
    }//GEN-LAST:event_titulo1MousePressed

    private void descripcion1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_descripcion1MousePressed
        if(descripcion1.getText().equals("Descripcion")){
            descripcion1.setText("");
            descripcion1.setForeground(Color.black);
        }
        if(titulo1.getText().isEmpty()){
            titulo1.setText("Nombre");
            titulo1.setForeground(Color.gray);
        }
    }//GEN-LAST:event_descripcion1MousePressed

