    public tareaInfo recuperarDatosGUI(){
        tareaInfo oTarea = new tareaInfo();
        
        Date fecha = (Date) fecha1.getValue();
        SimpleDateFormat dF = new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
        String strDate = dF.format(fecha);
        System.out.println(strDate);
        oTarea.setFecha(strDate);
        
        oTarea.setActividad(titulo1.getText());
        oTarea.setDescripcion(descripcion1.getText());
        return oTarea;
    }
    
